package p_order;

public class P_order {
	private String p_orderno;
	private String p_address;
	private String p_request;
	private String p_order_qty;
	private String p_total;
	private String id;
	private String p_code;
	private String p_name;
	
	public String getP_name() {
		return p_name;
	}
	public void setP_name(String p_name) {
		this.p_name = p_name;
	}
	public String getP_orderno() {
		return p_orderno;
	}
	public void setP_orderno(String p_orderno) {
		this.p_orderno = p_orderno;
	}
	public String getP_address() {
		return p_address;
	}
	public void setP_address(String p_address) {
		this.p_address = p_address;
	}
	public String getP_request() {
		return p_request;
	}
	public void setP_request(String p_request) {
		this.p_request = p_request;
	}
	public String getP_order_qty() {
		return p_order_qty;
	}
	public void setP_order_qty(String p_order_qty) {
		this.p_order_qty = p_order_qty;
	}
	public String getP_total() {
		return p_total;
	}
	public void setP_total(String p_total) {
		this.p_total = p_total;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getP_code() {
		return p_code;
	}
	public void setP_code(String p_code) {
		this.p_code = p_code;
	}
}
